package com.ahmed.aziz.salattracker;

public class Salat {
    private String name;
    private int option;

    public void setName(String name){
        this.name = name;
    }

    public void setOption(int option){
        this.option = option;
    }

    public String getName(){
        return name;
    }

    public int getOption(){
        return option;
    }
}
